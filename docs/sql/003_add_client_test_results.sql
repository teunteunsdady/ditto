-- 검사 결과 저장 스키마
-- 용어 정리:
--   - 테이블: client_assessments
--   - 검사 구분 컬럼: test_slug
--   - 결과 데이터 컬럼: result_data
-- 기존 curriculums 테이블을 이미 사용 중이면 자동으로 rename/migrate 합니다.

do $$
begin
  if exists (
    select 1
    from information_schema.tables
    where table_schema = 'public'
      and table_name = 'curriculums'
  ) and not exists (
    select 1
    from information_schema.tables
    where table_schema = 'public'
      and table_name = 'client_assessments'
  ) then
    execute 'alter table public.curriculums rename to client_assessments';
  end if;
end
$$;

create table if not exists public.client_assessments (
  id bigint generated by default as identity primary key,
  client_id uuid not null references public.clients(id) on delete cascade,
  test_slug varchar(50) not null,
  result_data jsonb not null default '{}'::jsonb,
  result_image_url text,
  assessment_status varchar(20) not null default 'pending',
  created_at timestamptz not null default now(),
  created_admin_id uuid references public.admins(id) on delete set null,
  updated_at timestamptz not null default now(),
  updated_admin_id uuid references public.admins(id) on delete set null
);

do $$
begin
  if exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'client_assessments' and column_name = 'curriculum_type'
  ) then
    execute 'alter table public.client_assessments rename column curriculum_type to test_slug';
  end if;

  if exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'client_assessments' and column_name = 'content'
  ) then
    execute 'alter table public.client_assessments rename column content to result_data';
  end if;

  if exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'client_assessments' and column_name = 'image_url'
  ) then
    execute 'alter table public.client_assessments rename column image_url to result_image_url';
  end if;

  if exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'client_assessments' and column_name = 'status'
  ) then
    execute 'alter table public.client_assessments rename column status to assessment_status';
  end if;

  if exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'client_assessments' and column_name = 'created_by'
  ) then
    execute 'alter table public.client_assessments rename column created_by to created_admin_id';
  end if;

  if exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'client_assessments' and column_name = 'updated_by'
  ) then
    execute 'alter table public.client_assessments rename column updated_by to updated_admin_id';
  end if;
end
$$;

alter table public.client_assessments
  alter column result_data set default '{}'::jsonb,
  alter column assessment_status set default 'pending';

create unique index if not exists client_assessments_client_test_uidx
  on public.client_assessments (client_id, test_slug);

create index if not exists client_assessments_client_id_idx
  on public.client_assessments (client_id);

create index if not exists client_assessments_test_slug_idx
  on public.client_assessments (test_slug);

create index if not exists client_assessments_status_idx
  on public.client_assessments (assessment_status);

drop trigger if exists trg_curriculums_updated_at on public.client_assessments;
drop trigger if exists trg_client_assessments_updated_at on public.client_assessments;
create trigger trg_client_assessments_updated_at
before update on public.client_assessments
for each row
execute function public.set_updated_at();

alter table public.client_assessments enable row level security;

drop policy if exists curriculums_service_role_all on public.client_assessments;
drop policy if exists client_assessments_service_role_all on public.client_assessments;
create policy client_assessments_service_role_all
on public.client_assessments
for all
to public
using (auth.role() = 'service_role')
with check (auth.role() = 'service_role');
